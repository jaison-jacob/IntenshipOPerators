package Operators;

public class AssignmentOperators {

   public static void main(String arg[]){
	   
	   int num1 = 67;
	   int num2 = 34;
	   
	   //"=" assignment operator use 
	   num2 = num1;
	   
	   System.out.println("= assignment = "+num2);
	   
	   //"+=" assignment use
	   
	   num2 += num1;
	   
	   System.out.println("+= assignment = "+num2);
	   
	   //"-=" assignment operator use 
	   
	    num1 = 67;
	    num2 = 34;
	   
	   num2 -= num1;
	   
	   System.out.println("-= assignment = "+num2);
	   
	 //"*=" assignment operator use 
	   
	    num1 = 67;
	    num2 = 34;
	   
	   num2 *= num1;
	   
	   System.out.println("*= assignment = "+num2);
	   
	 //"%=" assignment operator use 
	   
	    num1 = 67;
	    num2 = 34;
	   
	   num2 %= num1;
	   
	   System.out.println("%= assignment = "+num2);
	   
   }
	
	
	
}
