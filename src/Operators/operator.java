package Operators;

public class operator {

	public static void main(String arg[]){
		
		//Basic Arithmetic Operators
		
		int num1 = 5;
		int num2 = 6;
		
		// "+" operator
		
		System.out.println("num1 and num2 add ="+  (num1 + num2));
		
		//"-" oerator
		
		System.out.println("num1 and num2 substract =" + (num1 - num2));
		
		// "*" operator
		
		System.out.println("num1 and num2 multiply ="+(num1 * num2));
		
		//"/" operator
		
		System.out.println("num1 and num2 divide ="+(num1 * num2));
		
		
	}
	
}
